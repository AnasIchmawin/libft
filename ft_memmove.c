#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*d;
	char	*s;
	size_t	i;

	i = 0;
	d = (char *)dst;
	s = (char *)src;
	if (dst == src)
		return (dst);
	if (s < d)
	{
		while (len--)
			d[len] = s[len];
		return (dst);
	}
	while (len--)
	{
		d[i] = s[i];
		i++;
	}
	return (dst);
}
